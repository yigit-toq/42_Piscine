#include <unistd.h>

int	main(int argl, char **argv)
{
	int	i;

	i = 0;
	while (argv[0][i] != '\0' && argl)
	{
		write(1, &argv[0][i], 1);
		i++;
	}
	write(1, "\n", 1);
	return (0);
}
